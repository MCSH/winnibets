export default function GlyphPet({
  hash,
  seed = 0,
  size = 40,
  className = "",
}: {
  hash: string;
  seed?: number;
  size?: number;
  className?: string;
}) {
  const pixels = useMemo(() => {
    // Combine hash + seed for avatar variation
    const combined = hashCode(hash + String(seed));
    // Use the hash itself for more entropy
    const h = hash.replace(/[^a-f0-9]/gi, "").toLowerCase().padEnd(16, "0");

    // Pick shapes and colors using combined seed + hash digits
    const headShape = (digit(combined, 0) + parseInt(h[0], 16)) % HEADS.length;
    const headColor = COLORS[(digit(combined, 1) + parseInt(h[1], 16)) % 16];
    const eyeShape = (digit(combined, 2) + parseInt(h[2], 16)) % EYES.length;
    const eyeColor = COLORS[(digit(combined, 3) + parseInt(h[3], 16)) % 16];
    const mouthShape = (digit(combined, 4) + parseInt(h[4], 16)) % MOUTHS.length;
    const mouthColor = COLORS[(digit(combined, 5) + parseInt(h[5], 16)) % 16];
    const earShape = (digit(combined, 6) + parseInt(h[6], 16)) % EARS.length;
    const earColor = COLORS[(digit(combined, 7) + parseInt(h[7], 16)) % 16];
    const penisShape = (digit(combined, 8) + parseInt(h[8], 16)) % PENISES.length;
    const penisColor = COLORS[(digit(combined, 9) + parseInt(h[9], 16)) % 16];

    const result: { x: number; y: number; color: string }[] = [];

    // Draw order: head, ears, penis, then eyes and mouth on top
    for (const [x, y] of HEADS[headShape]) result.push({ x, y, color: headColor });
    for (const [x, y] of EARS[earShape]) result.push({ x, y, color: earColor });
    for (const [x, y] of PENISES[penisShape]) result.push({ x, y, color: penisColor });
    for (const [x, y] of EYES[eyeShape]) result.push({ x, y, color: eyeColor });
    for (const [x, y] of MOUTHS[mouthShape]) result.push({ x, y, color: mouthColor });

    return result;
  }, [hash, seed]);

  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 16 16"
      className={className}
      style={{ imageRendering: "pixelated" }}
      shapeRendering="crispEdges"
    >
      {pixels.map((p, i) => (
        <rect key={i} x={p.x} y={p.y} width={1} height={1} fill={p.color} />
      ))}
    </svg>
  );
}
