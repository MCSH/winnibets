export default function GlyphPet({
  hash,
  size = 40,
  className = "",
}: {
  hash: string;
  size?: number;
  className?: string;
}) {
  const pixels = useMemo(() => {
    const h = hash.replace(/[^a-f0-9]/gi, "").toLowerCase().padEnd(16, "0");

    // 8 hash digits → 4 components × (shape, color)
    const headShape = hexAt(h, 0) % HEADS.length;
    const headColor = COLORS[hexAt(h, 1)];
    const bodyShape = hexAt(h, 2) % BODIES.length;
    const bodyColor = COLORS[hexAt(h, 3)];
    const legShape = hexAt(h, 4) % LEGS.length;
    const legColor = COLORS[hexAt(h, 5)];
    const accShape = hexAt(h, 6) % ACCESSORIES.length;
    const accColor = COLORS[hexAt(h, 7)];

    const result: { x: number; y: number; color: string }[] = [];

    for (const [x, y] of LEGS[legShape]) {
      result.push({ x, y, color: legColor });
    }
    for (const [x, y] of BODIES[bodyShape]) {
      result.push({ x, y, color: bodyColor });
    }
    for (const [x, y] of HEADS[headShape]) {
      result.push({ x, y, color: headColor });
    }
    for (const [x, y] of ACCESSORIES[accShape]) {
      result.push({ x, y, color: accColor });
    }

    return result;
  }, [hash]);

  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 16 16"
      className={`${className}`}
      style={{ imageRendering: "pixelated" }}
      shapeRendering="crispEdges"
    >
      {pixels.map((p, i) => (
        <rect
          key={i}
          x={p.x}
          y={p.y}
          width={1}
          height={1}
          fill={p.color}
        />
      ))}
    </svg>
  );
}
