/**
 * GlyphPet — a 16x16 pixel creature with 5 body components:
 * head, eyes, mouth, ears, penis.
 * Each component has a shape and color derived from hash + seed.
 */

function hashCode(s: string): number {
  let h = 0;
  for (let i = 0; i < s.length; i++) {
    h = ((h << 5) - h + s.charCodeAt(i)) | 0;
  }
  return Math.abs(h);
}

function digit(n: number, i: number): number {
  return (n >> (i * 4)) & 0xf;
}

